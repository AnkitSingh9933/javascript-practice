/*

Topic: JavaScript Object Literal

What I learned today:
- Object stores data in key-value pairs.
- Object literal is the simplest way to create an object.
- An object can store different data types like string, number, and array.
*/

var student = {
  name: "Ankit",
  age: 27,
  subjects: ["Hindi", "English", "Math"]
};

// Accessing object properties
console.log(student.name);
console.log(student.age);
console.log(student.subjects);
console.log(student.subjects[0]);
